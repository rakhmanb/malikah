<h1>
<small>
Malikah is a family-owned company providing clothing for modest-minded women. It was founded by Ayu Rahmany and her husband, Bima Rakhman in 2017. Currently in the process of registering in the US, headquartered in Beautiful Snoqualmie Washington.
</small>
</h1>
<h1>
<small>
Malikah, which means “Queen” in Arabic, was chosen because in Islam women have high status and every one of them is a queen for her family. Designed by Ayu Rahmany, combining feminine and elegant lines with unique patterns and a touch of Indonesian tradition, using the highest quality of fabrics that pair seamlessly with our elegant design. 
</small>
</h1>
<h1>
<small>
Malikah's collections are all designed with the modest style in mind, because at Malikah, we dedicate our collection to both Muslim women who want to stay active in their daily activities but still look classy and elegant at the same time, as well as any woman who wants to keep a modest look so they can let their inner beauty shine. Malikah is here to show the beauty and universality of modest wear.
</small>
</h1>
